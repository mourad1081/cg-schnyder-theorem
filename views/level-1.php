	    <p>
            Before digging further to the limbos of the Schynder's theorem, 
            one must first see some notions in order to understand it clearly.
        </p>
        <p>
            The first one is <strong>the order</strong>.
        </p>
        <p>
            An order is a notion which enable us to compare elements of a set with each others.
            One define it as following:
        </p>
        <p>
            An order is a binary relation which is reflexive, antisymmetric and transitive.
            Let \(E\) be a set. An internal relation \( \leq \) on \( E \) is called an order relation if
            for all elements \( x, y, z \in E \), we have : 
            <ul>
                <li>\( x \leq x \), which is the reflexivity. An element is related, comparable to itself.</li>
                <li>\( (x \leq y \wedge y \leq x) \Rightarrow x = y \), which is the antisymmetry. Two different elements cannot be related in two different directions, otherwise they must be equal.</li>
                <li>\( (x \leq y \wedge y \leq z) \Rightarrow x \leq z \)</li>
            </ul>
        </p>
        <hr>
        <img src="img/france.svg" alt="français :" width="32px" height="32px">
        <p>
            Avant de s'aventurer plus profondément dans les limbes du théorème de Schnyder,
            nous devons d'abord voir quelques notions afin de le comprendre clairement.
        </p>
        <p>
            La première notion que nous allons voir s'appelle <strong>l'ordre</strong>.
        </p>
        <p>
            Une ordre est une notion nous permettant de comparer différents éléments d'un ensemble
            entre eux, il s'agit d'une relation binaire entre deux éléments d'un ensemble.
            Elle est définit ainsi : 
        </p>
        <p>
            Un ordre est une relation binaire réflexive, antisymmétrique et transitive.
            Soit \(E\) un ensemble. Une relation interne \( \leq \) sur \(E\) est appelée 
            <em>une relation d'ordre</em> si pour chaque éléments \( x, y, z \in E \), nous avons :
            <ul>
                <li>
                    \( x \leq x \), qui définit la réfléxivité. 
                    C'est-à-dire qu'un élément est en relation/comparable avec lui-même.
                </li>
                <li>
                    \( (x \leq y \wedge y \leq x) \Rightarrow x = y \), qui définit l'antisymmétrie. 
                    Deux éléments différents ne peuvent pas être reliées dans deux directions différentes
                     à moins d'être toutes les deux les mêmes.
                </li>
                <li>
                    \( (x \leq y \wedge y \leq z) \Rightarrow x \leq z \)
                </li>
            </ul>
        </p>
